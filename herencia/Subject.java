public class Subject {
    
    private String name_curse = "";
    private String hours_week = "";
    
    public Subject(String name_curse, String hours_week) {
        this.name_curse = name_curse;
        this.hours_week = hours_week;
    }

    public String getName_curse() {
        return name_curse;
    }

    public String getHours_week() {
        return hours_week;
    }

}
